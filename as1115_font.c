uint8_t AS1115_GetCharPattern(char c, uint8_t column)
{
    if (column > 7) return 0x00;

    if (c >= '0' && c <= '9')
        return font_digits[c - '0'][column];

    if (c >= 'A' && c <= 'Z')
        return font_alpha[c - 'A'][column];

    return 0x00;
}
